export async function getEvents() {
  const { rows } = await pool.query(`
        SELECT
            evt_id, 
            evt_icon, 
            evt_name, 
            evt_description, 
            evt_created_at, 
            evt_updated_at, 
            evt_is_use
        FROM events
        WHERE evt_is_use = true
        ORDER BY evt_id ASC;
    `);

  return rows.map(Mapping.mapEventsToSaveResponse);
}

